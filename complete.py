def train_jscc(C_out, snr_db, epochs=EPOCHS, steps=STEPS):
    """Build and train one (encoder, decoder) pair."""
    enc = build_encoder(C_out)
    dec = build_decoder(C_out)

    inp    = layers.Input(shape=(IMG_H, IMG_W, IMG_C))
    z      = enc(inp)
    z_norm = PowerNorm()(z)
    y      = AWGN(snr_db)(z_norm)
    out    = dec(y)
    model  = tf.keras.Model(inp, out)
    model.compile(optimizer=tf.keras.optimizers.Adam(1e-4), loss='mse')

    callbacks = [
        tf.keras.callbacks.ReduceLROnPlateau(
            monitor='loss', factor=0.5, patience=5,
            min_lr=1e-7, verbose=0),
        tf.keras.callbacks.EarlyStopping(
            monitor='loss', patience=10,
            restore_best_weights=True, verbose=0),
    ]
    model.fit(train_ds, steps_per_epoch=steps, epochs=epochs,
              callbacks=callbacks, verbose=0)
    return enc, dec


