import numpy as np

def jpeg_compress(img_u8, max_bytes):
    """Binary-search JPEG quality to fit within max_bytes. Returns bytes or None."""
    if max_bytes < 50:
        return None
    lo, hi, best = 1, 95, None
    while lo <= hi:
        mid = (lo + hi) // 2
        buf = io.BytesIO()
        Image.fromarray(img_u8).save(buf, format='JPEG', quality=mid)
        data = buf.getvalue()
        if len(data) <= max_bytes:
            best = data; lo = mid + 1
        else:
            hi = mid - 1
    return best


def bpsk_ber(snr_db):
    """Uncoded BPSK BER = 0.5 × erfc(√SNR_linear)."""
    return 0.5 * erfc(np.sqrt(10.0 ** (snr_db / 10.0)))


def bpsk_transmit(data_bytes, n_channel_bits, ber):
    """Flip bits with probability BER. Returns corrupted bytes."""
    bits   = np.unpackbits(np.frombuffer(data_bytes, dtype=np.uint8))
    n      = min(len(bits), n_channel_bits)
    bits   = bits[:n]
    errors = np.random.binomial(1, ber, n).astype(np.uint8)
    recv   = np.bitwise_xor(bits, errors)
    return np.packbits(recv).tobytes()


def jpeg_decode(data_bytes, H, W):
    """Safely decode JPEG; return black image if corrupted."""
    try:
        img = Image.open(io.BytesIO(data_bytes))
        img.load()
        arr = np.array(img.convert('RGB'))
        if arr.shape == (H, W, 3):
            return arr.astype(np.float32) / 255.0
    except Exception:
        pass
    return np.zeros((H, W, 3), dtype=np.float32)


def compute_psnr(orig, rec):
    """PSNR between two float32 [0,1] images."""
    mse = float(np.mean((orig - rec) ** 2))
    return 10.0 * np.log10(1.0 / mse) if mse > 1e-10 else 80.0


