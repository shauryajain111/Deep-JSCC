# ─── CELL 7 | Deep JSCC Architecture (Exact Figure 2 from Paper) ─────────────
import tensorflow as tf
from tensorflow.keras import layers, models

def build_encoder(C_out):
    inp = layers.Input(shape=(IMG_H, IMG_W, IMG_C))
    x   = layers.Lambda(lambda t: t * 2.0 - 1.0)(inp)           # [0,1] → [-1,1]
    x   = layers.Conv2D(16,    5, strides=2, padding='same')(x)  # 64→32
    x   = layers.PReLU(shared_axes=[1, 2])(x)
    x   = layers.Conv2D(32,    5, strides=2, padding='same')(x)  # 32→16
    x   = layers.PReLU(shared_axes=[1, 2])(x)
    x   = layers.Conv2D(32,    5, strides=1, padding='same')(x)  # 16×16×32
    x   = layers.PReLU(shared_axes=[1, 2])(x)
    x   = layers.Conv2D(32,    5, strides=1, padding='same')(x)  # 16×16×32
    x   = layers.PReLU(shared_axes=[1, 2])(x)
    x   = layers.Conv2D(C_out, 5, strides=1, padding='same')(x)  # 16×16×C_out
    out = layers.PReLU(shared_axes=[1, 2])(x)
    return models.Model(inp, out, name=f'Enc_C{C_out}')


def build_decoder(C_out):
    inp = layers.Input(shape=(H_LAT, W_LAT, C_out))
    x   = layers.Conv2DTranspose(32, 5, strides=1, padding='same')(inp)
    x   = layers.PReLU(shared_axes=[1, 2])(x)
    x   = layers.Conv2DTranspose(32, 5, strides=1, padding='same')(x)
    x   = layers.PReLU(shared_axes=[1, 2])(x)
    x   = layers.Conv2DTranspose(32, 5, strides=1, padding='same')(x)
    x   = layers.PReLU(shared_axes=[1, 2])(x)
    x   = layers.Conv2DTranspose(16, 5, strides=2, padding='same')(x)  # 16→32
    x   = layers.PReLU(shared_axes=[1, 2])(x)
    out = layers.Conv2DTranspose(3,  5, strides=2, padding='same',
                                  activation='sigmoid')(x)               # 32→64
    return models.Model(inp, out, name=f'Dec_C{C_out}')


class PowerNorm(layers.Layer):
    """Normalize encoder output to unit average power."""
    def call(self, z):
        return z / tf.sqrt(tf.reduce_mean(tf.square(z)) + 1e-8)


class AWGN(layers.Layer):
    """AWGN Channel: y = z + noise, noise ~ N(0, sigma²)."""
    def __init__(self, snr_db, **kw):
        super().__init__(**kw)
        self.sigma = float(np.sqrt(1.0 / (2.0 * 10.0 ** (snr_db / 10.0))))
    def call(self, z):
        return z + self.sigma * tf.random.normal(tf.shape(z))


