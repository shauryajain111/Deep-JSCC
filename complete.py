# ─── CELL 10 | Evaluate Deep JSCC — Figure 3 ─────────────────────────────────
def eval_jscc(enc, dec, snr_db, n_eval=N_EVAL):
    """Encode → Power Normalize → AWGN at test SNR → Decode → PSNR."""
    sigma  = float(np.sqrt(1.0 / (2.0 * 10.0 ** (snr_db / 10.0))))
    idx    = np.random.choice(TOTAL, n_eval, replace=False)
    psnrs  = []
    for i in range(0, len(idx), 50):
        batch = all_images[idx[i:i+50]].astype(np.float32) / 255.0
        z     = enc.predict(batch, verbose=0)
        # Power normalize
        z_n   = z / (np.sqrt(np.mean(z**2)) + 1e-8)
        # Add AWGN noise
        y     = z_n + np.random.normal(0, sigma, z_n.shape).astype(np.float32)
        rec   = np.clip(dec.predict(y, verbose=0), 0.0, 1.0)
        for o, r in zip(batch, rec):
            psnrs.append(compute_psnr(o, r))
    return float(np.mean(psnrs))


